x = float(3)        # Converts the integer 3 to float, x will be 3.0
y = float(2.8)      # Converts the float 2.8 to float, y will be 2.8
z = float("3")      # Converts the string "3" to float, z will be 3.0
w = float("4.2")    # Converts the string "4.2" to float, w will be 4.2

print(x)  # Output: 3.0
print(y)  # Output: 2.8
print(z)  # Output: 3.0
print(w)  # Output: 4.2
